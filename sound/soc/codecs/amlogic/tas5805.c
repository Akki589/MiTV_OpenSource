static int tas5805m_parse_dt(
	struct tas5805m_priv *tas5805m,
	struct device_node *np)
{
	enum of_gpio_flags flags;
	int reset_pin;
	int pdn_pin;
	unsigned int temp;

	reset_pin = of_get_named_gpio_flags(np, "reset_pin", 0, &flags);
	if (!gpio_is_valid(reset_pin)) {
		pr_info("%s fail to get reset pin from dts!\n", __func__);
		tas5805m->pdata->reset_pin = -1;
	} else {
		pr_info("%s reset_pin = %d!\n", __func__, reset_pin);
		tas5805m->pdata->reset_pin = reset_pin;
		tas5805m->pdata->reset_pin_active_low =
				 flags & OF_GPIO_ACTIVE_LOW;
	}

	pdn_pin = of_get_named_gpio_flags(np, "pdn_pin", 0, &flags);
	if (!gpio_is_valid(pdn_pin)) {
		pr_info("%s fail to get pdn pin from dts!\n", __func__);
		tas5805m->pdata->pdn_pin = -1;
	} else {
		pr_info("%s pdn_pin = %d!\n", __func__, pdn_pin);
		tas5805m->pdata->pdn_pin = pdn_pin;
		tas5805m->pdata->pdn_pin_active_low =
				 flags & OF_GPIO_ACTIVE_LOW;
	}

	if (of_property_read_u32(np, "param_index", &temp))
		pr_err("thermal: read param_index failed\n");
	else
		tas5805m->param_index = temp;
	pr_info("%s param_index = %d!\n", __func__, tas5805m->param_index);

	return 0;
}

static int tas5805m_i2c_probe(struct i2c_client *i2c,
			      const struct i2c_device_id *id)
{
	struct tas5805m_priv *tas5805m;
	struct tas5805m_platform_data *pdata;
	int ret;

	pr_info("tas5805m_i2c_probe\n");

	tas5805m = devm_kzalloc(&i2c->dev, sizeof(struct tas5805m_priv),
			       GFP_KERNEL);
	if (!tas5805m)
		return -ENOMEM;

	tas5805m->regmap = devm_regmap_init_i2c(i2c, &tas5805m_regmap);
	if (IS_ERR(tas5805m->regmap)) {
		ret = PTR_ERR(tas5805m->regmap);
		dev_err(&i2c->dev, "Failed to allocate register map: %d\n",
			ret);
		return ret;
	}

	mutex_init(&tas5805m->mutex);

	pdata = devm_kzalloc(&i2c->dev,
				sizeof(struct tas5805m_platform_data),
				GFP_KERNEL);
	if (!pdata) {
		pr_err("%s failed to kzalloc for TAS5805M pdata\n", __func__);
		return -ENOMEM;
	}

	tas5805m->pdata = pdata;

	tas5805m_parse_dt(tas5805m, i2c->dev.of_node);

	init_tas5805m_gpio(tas5805m);

	dev_set_drvdata(&i2c->dev, tas5805m);

	ret = snd_soc_register_codec(&i2c->dev, &tas5805m_codec,
					 &tas5805m_dai, 1);
	if (ret != 0)
		dev_err(&i2c->dev, "Failed to register CODEC: %d\n", ret);

	pr_info("tas5805m_i2c_probe over\n");

	return ret;
}

