static int init_tas5805m_gpio(struct tas5805m_priv *tas5805m)
{
	struct tas5805m_platform_data *pdata = tas5805m->pdata;
	int value;

	if (pdata->pdn_pin > 0) {
		gpio_request_one(pdata->pdn_pin,
						 GPIOF_OUT_INIT_LOW,
						 "tas5805m-reset-pin");

		value = pdata->pdn_pin_active_low ?
			GPIOF_OUT_INIT_LOW : GPIOF_OUT_INIT_HIGH;

		gpio_direction_output(pdata->pdn_pin,
				value);

		mdelay(1);

		value = pdata->pdn_pin_active_low ?
			GPIOF_OUT_INIT_HIGH : GPIOF_OUT_INIT_LOW;

		gpio_direction_output(pdata->pdn_pin,
				value);

		mdelay(1);
	}

	return 0;
}

static int tas5805m_init(struct snd_soc_codec *codec, unsigned int index)
{
	int i;
	struct tas5805m_priv *tas5805m = snd_soc_codec_get_drvdata(codec);

	dev_info(codec->dev, "tas5805m_init, index = %d!\n", index);

	mutex_lock(&tas5805m->mutex);

	for (i = 0; i < sizeof(tas5805m_reset_regs)/2 ; i++) {
		snd_soc_write(codec, tas5805m_reset_regs[i][0],
					tas5805m_reset_regs[i][1]);
	}

	mdelay(20);

	switch (index) {
	case 1:
		for (i = 0; i < sizeof(tas5805m_v77i_regs)/2 ; i++) {
			snd_soc_write(codec, tas5805m_v77i_regs[i][0],
						 tas5805m_v77i_regs[i][1]);
		}
		break;
	default:
		for (i = 0; i < sizeof(tas5805m_v77i_regs)/2 ; i++) {
			snd_soc_write(codec, tas5805m_v77i_regs[i][0],
						 tas5805m_v77i_regs[i][1]);
		}
		break;
	}

	mutex_unlock(&tas5805m->mutex);

	return 0;
}

static int tas5805m_probe(struct snd_soc_codec *codec)
{
	struct tas5805m_priv *tas5805m = snd_soc_codec_get_drvdata(codec);

#ifdef CONFIG_MITV_AUDIO_CODEC_DEBUG
	int ret;
#endif

#ifdef CONFIG_HAS_EARLYSUSPEND
	early_suspend.level = EARLY_SUSPEND_LEVEL_BLANK_SCREEN;
	early_suspend.suspend = tas5805m_early_suspend;
	early_suspend.resume = tas5805m_late_resume;
	early_suspend.param = codec;
	register_early_suspend(&early_suspend);
#endif

	tas5805m_init(codec, tas5805m->param_index);

#ifdef CONFIG_MITV_AUDIO_CODEC_DEBUG
	tas5805m->codec = codec;
	ret = sysfs_create_group(&codec->dev->kobj,
		&tas5805m_group);
	if (ret < 0)
		dev_err(codec->dev, "Create sys fs node fail: %d\n", ret);
	else {
		if (xiaomi_get_speaker_class() != NULL) {
			ret = class_compat_create_link_with_name(
				xiaomi_get_speaker_class(),
				codec->dev,
				"AMP");
			if (ret)
				dev_warn(codec->dev,
					"Failed to create compatibility class link\n");
		}
	}

#endif

	return 0;
}

static int tas5805m_remove(struct snd_soc_codec *codec)
{
#ifdef CONFIG_MITV_AUDIO_CODEC_DEBUG
	sysfs_remove_group(&codec->dev->kobj,
		&tas5805m_group);
	if (xiaomi_get_speaker_class() != NULL)
		class_compat_remove_link_with_name(xiaomi_get_speaker_class(),
			codec->dev,
			"AMP");
#endif
#ifdef CONFIG_HAS_EARLYSUSPEND
	unregister_early_suspend(&early_suspend);
#endif

	return 0;
}

static int tas5805m_set_bias_level(struct snd_soc_codec *codec,
				  enum snd_soc_bias_level level)
{
	pr_debug("level = %d\n", level);

	switch (level) {
	case SND_SOC_BIAS_ON:
		break;

	case SND_SOC_BIAS_PREPARE:
		/* Full power on */
		break;

	case SND_SOC_BIAS_STANDBY:
		break;

	case SND_SOC_BIAS_OFF:
		/* The chip runs through the power down sequence for us. */
		break;
	}
	codec->component.dapm.bias_level = level;

	return 0;
}


