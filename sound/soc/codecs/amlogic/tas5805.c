static int tas5805m_trigger(struct snd_pcm_substream *substream, int cmd,
			       struct snd_soc_dai *codec_dai)
{
	struct tas5805m_priv *tas5805m = snd_soc_dai_get_drvdata(codec_dai);
	struct snd_soc_codec *codec = tas5805m->codec;

	if (substream->stream == SNDRV_PCM_STREAM_PLAYBACK) {
		switch (cmd) {
		case SNDRV_PCM_TRIGGER_START:
		case SNDRV_PCM_TRIGGER_RESUME:
		case SNDRV_PCM_TRIGGER_PAUSE_RELEASE:
			pr_debug("%s(), start\n", __func__);
			mutex_lock(&tas5805m->mutex);
			snd_soc_write(codec, DDX_CLOCK_CTL, 0x00);
			snd_soc_write(codec, DDX_BOOK_CHANGE, 0x00);
			snd_soc_write(codec, DDX_CLOCK_CTL, 0x00);
			snd_soc_write(codec, DDX_DIG_VAL_CTL, 0x30);
			mutex_unlock(&tas5805m->mutex);
			break;
		case SNDRV_PCM_TRIGGER_STOP:
		case SNDRV_PCM_TRIGGER_SUSPEND:
		case SNDRV_PCM_TRIGGER_PAUSE_PUSH:
			pr_debug("%s(), stop\n", __func__);
			mutex_lock(&tas5805m->mutex);
			snd_soc_write(codec, DDX_CLOCK_CTL, 0x00);
			snd_soc_write(codec, DDX_BOOK_CHANGE, 0x00);
			snd_soc_write(codec, DDX_CLOCK_CTL, 0x00);
			snd_soc_write(codec, DDX_DIG_VAL_CTL, 0xff);
			mutex_unlock(&tas5805m->mutex);
			break;
		}
	}

	return 0;
}

